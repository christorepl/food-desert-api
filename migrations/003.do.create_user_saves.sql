CREATE TABLE user_saves (
    save_id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    save_name VARCHAR(255) NOT NULL,
    user_id UUID NOT NULL,
    FOREIGN KEY (user_id) REFERENCES users(user_id),
    modified TIMESTAMPTZ NOT NULL,
    state_name TEXT NOT NULL UNIQUE,
    fips NUMERIC NOT NULL UNIQUE,
    state_abbrev TEXT NOT NULL UNIQUE,
    poverty_rate NUMERIC NOT NULL,
    food_insecurity_rate NUMERIC NOT NULL,
    white NUMERIC NOT NULL,
    black NUMERIC NOT NULL,
    hispanic NUMERIC NOT NULL,
    asian NUMERIC NOT NULL,
    mixed_race NUMERIC NOT NULL,
    other NUMERIC NOT NULL,
    trump NUMERIC NOT NULL,
    biden NUMERIC NOT NULL,
    ranking_repub INTEGER NOT NULL UNIQUE,
    ranking_dem INTEGER NOT NULL UNIQUE,
    ranking_white INTEGER NOT NULL UNIQUE,
    ranking_black INTEGER NOT NULL UNIQUE,
    ranking_hispanic INTEGER NOT NULL UNIQUE,
    ranking_asian INTEGER NOT NULL UNIQUE,
    ranking_mixed INTEGER NOT NULL UNIQUE,
    ranking_other INTEGER NOT NULL UNIQUE,
    ranking_pov INTEGER NOT NULL UNIQUE,
    ranking_fi INTEGER NOT NULL UNIQUE
);